<h2>Qui sommes-nous ?</h2>
Création de CLAIRALU en 1988.</br> 
Notre société a été créé en Mars 1988 par Mr Clair Jean-François afin de répondre à un besoin de pliage sur commande.</br>
Nous sommes installés dans le petit village de Pesmes en Haute-Saône (70).</br> 
Avec un effectif de huit employés.</br>
